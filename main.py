i = 0 # счетчик для цикла
print("(k, l) и (m, n) - координаты двух полей шахматной доски от 1 до 8.")
coordinates = ["k", "l", "m", "n"]
coor = [] # список для введенных значений
# цикл для ввода и проверки значений пользователя
while i < 4:
  try:
    s = int(input("Введите %s: " % coordinates[i]))
    if (s < 1) or (s > 8):
      raise ValueError()
    i += 1
    coor.append(s)
  except ValueError:
    print("Неправильное значение!")
# присваивание значений переменным
k, l, m, n = coor[0], coor[1], coor[2], coor[3]
print("Введенные поля: (", k, ",", l, ") и (", m, ",", n,").\n")

print("Ответы:")
# пункт а
# проверка на одинковый цвет полей
if (((k+l)%2)==((m+n)%2)):
  print("Поля (", k, ",", l, ") и (", m, ",", n,") одного цвета.")
else:
  print("Поля (", k, ",", l, ") и (", m, ",", n, ") разного цвета.")
print()

# пункт б
# проверка на угрозу ферзём
if ((k==l) or (m==n) or (k+l==m+n) or (k-l==m-n)):
  print("Ферзь с поля (", k, ",", l, ") угрожает полю (", m, ",", n, ").")
else:
  print("Ферзь с поля (", k, ",", l, ") не угрожает полю (", m, ",", n, ").")
print()

