# пункт д
# проверка на доступность второго поля для ферзя
if ((k==m) or (l==n) or (k+l==m+n) or (k-l==m-n)):
  print("Можно одним ходом ферзя попасть с поля (", k, ",", l, ") на поле (", m, ",", n, ").")
else:
  print("Можно за два хода ферзя попасть с поля (", k, ",", l, ") на поле (", m, ",", n, ").")
  print("Промежуточный ход на поле (", k, ",", n, ").")
